import java.util.Scanner;

public class Strings_tut13 {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        String s=new String("Sarthak");
        System.out.println(s);
        s=sc.next();
        System.out.println(s);
        s=sc.nextLine();
        System.out.println(s);
    }
}
